<h3>Çocuklara Yönelik Politikamız</h3>

<p>Hizmetlerimiz, 13 yaşından küçüklere yönelik değildir. Çocuğunuzun, rızanız olmadan kişisel bilgileri bize sağladığının farkına varırsanız, lütfen bizimle iletişime geçin. 13 yaşından küçük çocuklardan isteyerek kişisel bilgi toplamayız. 13 yaşından küçük bir çocuğun bize kişisel bilgi sağladığının farkına varırsak, bu tür bilgileri kaldırmak ve çocuğun hesabını sonlandırmak için gerekli adımları atarız.</p>

<h3>Bu Politikadaki Değişiklikler</h3>
<p>Zaman zaman bu Gizlilik Politikasını revize edebiliriz. Politikanın en güncel sürümü bilgilerini nasıl kullanacağımızı belirleyecek ve her zaman Gizlilik linkinde olacaktır. Tamamen kendi takdirimize bağlı olarak, herhangi bir nedenle ya da nedensiz, bu politikada bir değişiklik yapabiliriz. Değişiklikler etkinleştikten sonra Hizmetlere erişmeye ve hizmetleri kullanmaya devam etmekle revize edilmiş Gizlilik Politikasına bağlı olmayı kabul etmiş olursun.</p>


