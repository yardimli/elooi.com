<p><b>Kayıt Sırasında Toplanan Bilgiler:</b> Bir Elooi hesabı oluşturduğunuzda ya da yeniden yapılandırdığınızda, adınız, soyadınız, şifreniz ve e-posta adresiniz gibi bazı kişisel bilgiler sağlarsınız. Bu bilgilerin bazıları, örneğin adınız ve soyadınız, profil sayfanızla arama sonuçları da dahil olmak üzere Hizmetlerimizde herkese açık olarak listelenecektir.</p>

<p><b>Ek Bilgi:</b> Kısa biyografi, konumunuz ya da resim gibi, herkese açacağımız ek bilgiler de sağlayabilirsiniz. İletişim bilgilerinizi, Hizmetlerimiz hakkında size bilgi göndermek veya pazarlama yapmak için kullanabiliriz. Bu mesajlara olan aboneliğinizi, mesajlar içindeki ya da web sitemizdeki yönergeleri izleyerek iptal edebilirsiniz.</p>

