<p><b>Konum Bilgisi:</b> Elooi profilinde ve Elooilerinde konumunu belirtmeyi seçebilirsin.</p>

<p><b>Veri Günlüğü:</b> Sunucularımız, Hizmetleri kullanımınızla oluşturulan bilgileri otomatik olarak kaydeder ("Veri Günlüğü"). Veri Günlüğü, IP adresiniz, tarayıcı türünüz, başvuran alan adınız, ziyaret edilen sayfalarınız ve arama terimleriniz gibi bilgileri içerebilir. Reklamlarla etkileşimler gibi diğer eylemler de Veri Günlüğüne dahil edilebilir. Veri Günlüğünü önceden silmediysek, ya silinecektir ya da 18 ay içinde kullanıcı adınız, tam IP adresiniz veya e-posta adresiniz gibi temel hesap belirliyicileri kaldırılacaktır.</p>

<p><b>Çerezler:</b> Çoğu web sitesi gibi, biz de, ek web sitesi kullanım verilerini toplamak ve Hizmetlerimizi geliştirmek için "çerez" teknolojisini kullanırız. Çerez, bilgisayarınızın sabit diskine aktarılan küçük bir veri dosyasıdır. Elooi, Hizmetlerimizle nasıl etkileşim kurduğunuzu daha iyi anlamak, kullanıcılarımızın toplu kullanımını ve Hizmetlerimizdeki web trafiğini izlemek ve Hizmetlerimizi geliştirmek için hem oturum çerezlerini hem de daimi çerezleri kullanabiliriz.</p>

<p><b>Üçüncü Parti Hizmetler:</b> Elooi, Google Analytics gibi çeşitli üçüncü parti servis sağlayıcıları kullanır. Bu hizmetler, gönderilen çerez ve IP talepleri gibi bir internet sitesi isteğinin parçası olarak tarayıcınız tarafından iletilen bilgileri toplayabilir.</p>

