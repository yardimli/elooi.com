<p><b>連結：</b>Elooi可能從轉導點擊或其他渠道追蹤您在推文和連結之間的互動。我們這樣做是為了改善服務，包括廣告在內，我們能從上述方法得到累計點擊次數的統計數據，例如知道某個連結被點擊了多少次等等。</p>

<p><b>Cookies: </b>正如很多網站所作，我們使用“cookies”技術收集額外的網站使用數據來改善服務，但在服務上的許多處，例如搜尋和觀看用戶個人檔案或列表，我們也不需要“cookies”。Cookies是一片從您電腦硬碟轉移的小型數據文件。Elooi或會同時使用session cookies（臨時性）和persistent cookies（長期性）來更了解您如何跟我們的服務互動、監察用戶的集體使用情況和本服務的網絡流量，以及改善本服務。大部份瀏覽器也自動允許cookies，您可以修改瀏覽器的選項來禁止允許cookies或在允許來自網頁的cookie前先給您提示。</p>

<p><b>第三方服務：</b>Elooi使用多個第三方提供的服務使本服務在各方面更完善，例如經營多個博客和線上共筆系統，運用Google Analytics來了解本服務的利用率等。這些服務可能收集了網頁要求您的瀏覽器發送的資訊如cookies和IP請求。</p>

