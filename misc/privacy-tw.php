<p><b>額外資訊：</b>你可以提供我們額外的公開資訊，例如簡短的自傳、你的位置或一張照片。你也可提供其他資訊如手機號碼來寄送簡訊，或通訊錄來讓我們協助你尋找你可能認識的Elooi用戶。我們或會使用該聯絡資訊發送本服務或有關市場的訊息給你。你可以依照訊息附帶的指示或我們網站上的說明來取消訂閱它。如果你寄電子郵件給我們，我們將會保留你的訊息、電子郵件地址及聯絡資訊以回應你的訴求。以上提及的所有額外資訊皆是非強制性的。</p>

<p style="margin-bottom:10px"><b>推文、關注、列表及其他公開資訊：</b>我們的服務主要是協助您向世界分享訊息。您提供的大部分資訊皆是您希望我們公開的。當中不僅包括您推的訊息和其元素據，也包括您建立的列表、您關注的人、您收藏的推文、轉推，以及很多細項。我們預設公開您提供的資訊，但一般也讓您在<a href="http://elooi.com/account/settings">設定(http://elooi.com/account/settings)</a>修改資料的公開權限。您所有的公開資訊會廣泛地即時散播，例如您公開的推文則能被許多搜尋引擎搜索，而且立即經簡訊和我們的<a href="http://dev.elooi.com/pages/api_faq">APIs(http://dev.elooi.com/pages/api_faq)</a>廣泛地發送至用戶和服務手上。因此不僅是推文，您也須多加注意從Elooi公開的所有資訊。</p>

